// NPM (Node Package Manage)
// npm global command comes with the Node
// npm --version
// npm -v

// local dependency - use only for a particular project
// npm install <packageName>
// npm i <packageName>

// global dependency - use for all the projects.
// npm install -g <packageName>
// npm i -g <packageName>

// package.json - manifest file (stores important info about project/packages)
// You can configure package.json in three ways:
// 1. manual apporach (create package.json in the root of the project)
// 2. npm init (step by step, press enter to skip)
// 3. npm init -y (everything default)

// save the packages as dev dependency
// npm install <packageName> --save-dev
// npm install <packageName> -D

